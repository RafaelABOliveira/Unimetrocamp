def soma(num1, num2):
    print(f'Vou somar {num1} com {num2}: ')
    return num1 + num2

def subtracao(num1, num2):
    print(f'Vou subtrair {num1} com {num2}: ')
    return num1 - num2


def multiplicacao(num1, num2):
    print(f'Vou multiplicar {num1} com {num2}: ')
    return num1 * num2


def divisao(num1, num2):
    print(f'Vou dividir {num1} com {num2}: ')
    return num1 / num2
